package com.github.paohaijiao.enums;

/**
 * 数据交换类型 - 定义数据在分布式节点间的传输策略
 *
 * <p>类型速查表：
 * <pre>
 * ┌─────────────┬──────────────────┬──────────────┬─────────────────────────┐
 * │ 类型        │ 数据流向         │ 数据量变化   │ 典型场景               │
 * ├─────────────┼──────────────────┼──────────────┼─────────────────────────┤
 * │ FORWARD     │ 单节点 → 单节点  │ 不变         │ 本地连续操作           │
 * │ SHUFFLE     │ 多节点 → 多节点  │ 不变         │ 按Key重新分区          │
 * │ REPARTITION │ 多节点 → 多节点  │ 不变         │ 负载均衡重分布         │
 * │ BROADCAST   │ 单节点 → 所有节点│ 放大N倍      │ 小表广播               │
 * │ GATHER      │ 多节点 → 单节点  │ 汇聚         │ 结果汇总               │
 * │ RECEIVE     │ 远程 → 本地      │ 接收         │ 接收数据（配对使用）   │
 * └─────────────┴──────────────────┴──────────────┴─────────────────────────┘
 * </pre>
 */
public enum JQuickExchangeType {
    /**
     * SHUFFLE - 哈希重分布
     * <p>
     * 数据流向：多节点 → 多节点
     * <p>
     * 数据量变化：不变
     * <p>
     * 分区策略：根据 Key 的哈希值决定目标节点，相同 Key 的数据保证进入同一节点
     * <p>
     * 网络开销：有（需要序列化和网络传输）
     * <p>
     * 典型场景：GROUP BY 聚合、JOIN 操作，需要相同 Key 的数据在一起计算
     */
    SHUFFLE,
    /**
     * BROADCAST - 广播
     * <p>
     * 数据流向：单节点 → 所有节点
     * <p>
     * 数据量变化：放大 N 倍（N = 节点数）
     * <p>
     * 网络开销：小表行数 × 节点数
     * <p>
     * 适用条件：小表（如维度表）数据量较小时使用
     * <p>
     * 典型场景：小表 JOIN 大表，将小表广播到所有节点，避免大表做 Shuffle
     */
    BROADCAST,
    /**
     * REPARTITION - 重新分区（负载均衡）
     * <p>
     * 数据流向：多节点 → 多节点
     * <p>
     * 数据量变化：不变
     * <p>
     * 分区策略：轮询或随机分配，只关心负载均衡，不关心 Key 的分布
     * <p>
     * 注意事项：相同 Key 的数据可能分散到不同节点
     * <p>
     * 典型场景：解决数据倾斜问题，将数据重新均匀分布到所有节点
     */
    REPARTITION,
    /**
     * GATHER - 汇聚
     * <p>
     * 数据流向：多节点 → 单节点
     * <p>
     * 数据量变化：汇聚（所有数据集中到一个节点，总量不变）
     * <p>
     * 网络开销：所有数据都需传输到目标节点
     * <p>
     * 注意事项：单节点可能成为性能瓶颈
     * <p>
     * 典型场景：LIMIT（取前N条）、ORDER BY + LIMIT（全局排序）、最终结果输出
     */
    GATHER,
    /**
     * RECEIVE - 接收数据（被动类型）
     * <p>
     * 数据流向：远程 → 本地
     * <p>
     * 数据量变化：接收（作为输入数据）
     * <p>
     * 角色定位：被动接收端，不主动发起传输，与 SHUFFLE/BROADCAST 配对使用
     * <p>
     * 典型场景：标记节点有数据输入，表示数据依赖关系
     * <p>
     * 注意事项：实际网络传输由发送端（SHUFFLE/BROADCAST）发起
     */
    RECEIVE,

    /**
     * FORWARD - 直接转发
     * <p>
     * 数据流向：单节点 → 单节点
     * <p>
     * 数据量变化：不变
     * <p>
     * 网络开销：无（同一节点内传递）
     * <p>
     * 典型场景：TableScan → Filter → Project 等流水线连续操作，数据不跨节点
     */
    PIPELINE
}
